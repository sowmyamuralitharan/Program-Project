package org.prog;

public class Pyramid {
	
	public static void main(String[] args)
	{
		
		System.out.println("========Pyramid========");
		
		for(int i=1; i<=5; i++)
		{
			for(int j=0; j<=i; j++)
			{
				System.out.println("* ");
			}
			System.out.println();
		}
		
		 
	}

}
